# Store a message in a variable and print it
message = "Challenge accepted"
print(message)
# Changing case of a name
first_name = "fanny"
# Just first letter uppercase
print(first_name.title())
# All letters uppercase
print(first_name.upper())
# All letters lowercase
print(first_name.lower())
# Combining strings
first_name = "fanny"
last_name = "scheltgen"
print(first_name.title() + " " + last_name.title())
# Whitespace
# Make a tab appear in a string
print("Hello \teveryone!")
# Make a newline appear in a string
print("Hello \neveryone!")
# Strip whitespace from the left side, right side and both sides of a string
name = " fanny "
print(name.lstrip())
print(name.rstrip())
print(name.strip())
# Arithmetic
# Integers addition, subtraction, multiplication, division and exponents
print(3+2)
print(5-2)
print(3*5)
print(6/3)
print(3**2)
# Use parenthesis to modify the order of operations
print((2+3)*4)
# Floating
print(0.1+0.1)

